     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {

        if ($request->search) {
            $games = Game::where('title', 'like', '%'.$request->search.'%')
            ->get ();
            return view('games.index', compact ('games'));
        }

        $games = Game::paginate(8);
        return view('games.index', compact('games'));
    }

