    {
        $title = $request->input('title');
        $poster = $request->input('poster');
        $url = $request->input('url');

        try {

            return Game::create(
                [
                    'title' => $title,
                    'poster' => $poster,
                    'url' => $url
                ]
                );

        } catch (QueryException $error) {
            $codigoError = $error->errorInfo[1];

            
                return response()->json([
                    'error' => $codigoError
                ]);
            
        }    }

